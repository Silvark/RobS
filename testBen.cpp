void explode(std::vector<std::vector<int>> & level,std::vector<sf::RectangleShape> & map,int posY,int posX,int radius){

    for(int i = posX - radius; i <= posX + radius; i++)
    {
        for(int j = posY - radius; j <= posY + radius; j++)
        {
            if((i-posX)*(i-posX) + (j-posY)*(j-posY) < radius*radius)
            {

                if(i > 0 && i < fenetrecasehauteur && j < fenetrecaselargeur && j > 0){
                    if(level[i][j] == 1){
                        level[i][j]= 0;
                    }
                }
           }
       }
   }
}

